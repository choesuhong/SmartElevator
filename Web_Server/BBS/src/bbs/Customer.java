package bbs;

public class Customer {
	private int customerID;
	private String customerName;
	private String customerPhoneNumber;
	private String customerRoomNumber;
	private String userID;
	private String customerET;
	private String customerPicture;
	private String customerFloor;
	private int customerAvailable;
	public int getCustomerID() {
		return customerID;
	}
	public void setCustomerID(int customerID) {
		this.customerID = customerID;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerPhoneNumber() {
		return customerPhoneNumber;
	}
	public void setCustomerPhoneNumber(String customerPhoneNumber) {
		this.customerPhoneNumber = customerPhoneNumber;
	}
	public String getCustomerRoomNumber() {
		return customerRoomNumber;
	}
	public void setCustomerRoomNumber(String customerRoomNumber) {
		this.customerRoomNumber = customerRoomNumber;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCustomerET() {
		return customerET;
	}
	public void setCustomerET(String customerET) {
		this.customerET = customerET;
	}
	public String getCustomerPicture() {
		return customerPicture;
	}
	public void setCustomerPicture(String customerPicture) {
		this.customerPicture = customerPicture;
	}
	public String getCustomerFloor() {
		return customerFloor;
	}
	public void setCustomerFloor(String customerFloor) {
		this.customerFloor = customerFloor;
	}
	public int getCustomerAvailable() {
		return customerAvailable;
	}
	public void setCustomerAvailable(int customerAvailable) {
		this.customerAvailable = customerAvailable;
	}
	
	
	
	
}
